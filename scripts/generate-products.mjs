import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const productsDir = path.join(root, "public", "images", "products");
const outFile = path.join(root, "src", "data", "products.ts");

const WHATSAPP = "919081622516";

const FOLDER_MAP = {
  "bath accesories": { slug: "bath-accessories", category: "Bath Accessories" },
  bedding: { slug: "bedding", category: "Bedding" },
  carpet: { slug: "carpet", category: "Carpets" },
  chairs: { slug: "chairs", category: "Chairs" },
  "curtains & blinds": { slug: "curtains-blinds", category: "Curtains & Blinds" },
  mats: { slug: "mats", category: "Mats" },
  Mettress: { slug: "mattress", category: "Mattresses" },
  Sofas: { slug: "sofas", category: "Sofas" },
  Tables: { slug: "tables", category: "Tables" },
  wallpapers: { slug: "wallpapers", category: "Wallpapers" },
};

const STYLE_POOL = ["Modern", "Classic", "Contemporary", "Minimal", "Editorial"];
const MATERIAL_POOL = {
  "bath-accessories": ["Cotton", "Microfiber", "Polyester"],
  bedding: ["Cotton", "Linen Blend", "Microfiber"],
  carpet: ["Wool Blend", "PVC", "Polypropylene"],
  chairs: ["Fabric", "Wood", "Upholstered"],
  "curtains-blinds": ["Polyester", "Blackout Fabric", "Sheer Blend"],
  mats: ["Rubber", "Grass Fiber", "Memory Foam"],
  mattress: ["Memory Foam", "Spring", "Hybrid Foam"],
  sofas: ["Velvet", "Linen Fabric", "Premium Fabric"],
  tables: ["Wood", "Glass & Wood", "Laminate"],
  wallpapers: ["Vinyl", "Non-Woven", "Textured Paper"],
};

function slugify(text) {
  return text
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function walk(dir, base = "") {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(full, rel));
    } else if (/\.(png|jpe?g|webp|gif)$/i.test(entry.name)) {
      files.push(rel.replace(/\\/g, "/"));
    }
  }
  return files;
}

function pickMaterial(categorySlug, index) {
  const pool = MATERIAL_POOL[categorySlug] ?? ["Premium Blend"];
  return pool[index % pool.length];
}

function pickStyle(index) {
  return STYLE_POOL[index % STYLE_POOL.length];
}

function inferDimensions(categorySlug) {
  const dims = {
    "bath-accessories": "Standard bath size",
    bedding: "King / Queen compatible",
    carpet: "Custom sizing available",
    chairs: "32″ W × 34″ D × 36″ H",
    "curtains-blinds": "Made to measure",
    mats: "Multiple sizes in-store",
    mattress: "72″ × 72″ (custom available)",
    sofas: "6′ × 8′ sectional",
    tables: "48″ × 24″",
    wallpapers: "Roll — 53 cm × 10 m",
  };
  return dims[categorySlug] ?? "See showroom for sizing";
}

function buildDescription(name, category) {
  return `Curated ${name.toLowerCase()} from our ${category} collection — crafted for refined interiors with lasting comfort and editorial presence.`;
}

const relFiles = walk(productsDir);
const usedSlugs = new Set();
const products = [];
let id = 1;

const featuredPerCategory = {};

for (const rel of relFiles.sort()) {
  const parts = rel.split("/");
  const topFolder = parts[0];
  const meta = FOLDER_MAP[topFolder];
  if (!meta) continue;

  const fileName = path.basename(rel, path.extname(rel));
  let baseSlug = slugify(fileName);
  let slug = `${meta.slug}-${baseSlug}`;
  if (usedSlugs.has(slug)) slug = `${slug}-${id}`;
  usedSlugs.add(slug);

  const subPath = parts.slice(1, -1).join("/");
  const folder = subPath ? `${topFolder}/${subPath}` : topFolder;
  const urlPath = `/images/products/${rel}`;

  const idx = products.length;
  if (!featuredPerCategory[meta.slug]) featuredPerCategory[meta.slug] = 0;
  const featured = featuredPerCategory[meta.slug] < 2;
  if (featured) featuredPerCategory[meta.slug]++;

  const tags = [meta.category, meta.slug];
  if (subPath) tags.push(slugify(subPath));

  products.push({
    id: String(id++),
    slug,
    name: fileName,
    category: meta.category,
    categorySlug: meta.slug,
    description: buildDescription(fileName, meta.category),
    material: pickMaterial(meta.slug, idx),
    dimensions: inferDimensions(meta.slug),
    featured,
    images: [urlPath],
    folder,
    style: pickStyle(idx),
    price: null,
    tags,
    whatsappNumber: WHATSAPP,
  });
}

const categories = Object.values(FOLDER_MAP).map((m) => ({
  slug: m.slug,
  name: m.category,
}));

const file = `/* eslint-disable */
// Auto-generated by scripts/generate-products.mjs — do not edit manually
import type { Product, ProductCategory } from "@/types/product";

export const productCategories: ProductCategory[] = ${JSON.stringify(categories, null, 2)};

export const products: Product[] = ${JSON.stringify(products, null, 2)};

export const featuredProducts = products.filter((p) => p.featured);
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, file, "utf8");
console.log(`Generated ${products.length} products → ${outFile}`);
